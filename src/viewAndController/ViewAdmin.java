    private void coutBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_coutBtnActionPerformed
        // TODO add your handling code here:
        Calendar Cal = Calendar.getInstance();
        int year = Cal.get(Calendar.YEAR);
        int month = Cal.get(Calendar.MONTH)+1;
        int day = Cal.get(Calendar.DAY_OF_MONTH);
        int hour = Cal.get(Calendar.HOUR_OF_DAY);
        int minute = Cal.get(Calendar.MINUTE);
        int second = Cal.get(Calendar.SECOND);

        String time = String.valueOf(second)+":"+String.valueOf(minute)+":"+String.valueOf(hour)+":"
        +" "+String.valueOf(day)+"/"+String.valueOf(month)+"/"+String.valueOf(year);

        if(typeFlower.equals("Hoa đơn")){
            for(singleFlower flower : listsinger){
                if(Codetxt.getText().equals(flower.getCode())){
                    if(Double.parseDouble(OrderOuttxt.getText()) <= flower.getAmount()){
                        listSaveOut.add(new listSaveInput(flower.getName(), flower.getCode(), flower.getType(), flower.getColor(), Double.parseDouble(OrderOuttxt.getText()), 0, flower.getPetalsFlowerNum(), time));
                        flower.setAmount(flower.getAmount() - Double.parseDouble(OrderOuttxt.getText()));
                        createTable(listsinger, listbanyan);
                    }else{
                        JOptionPane.showMessageDialog(rootPane, "Không đủ số lượng để xuất");
                    }
                }
            }
        }else{
            for(banyanFlower flower : listbanyan){
                if(Codetxt.getText().equals(flower.getCode())){
                    if(Double.parseDouble(OrderOuttxt.getText()) <= flower.getAmount()){
                        listSaveOut.add(new listSaveInput(flower.getName(), flower.getCode(), flower.getType(), flower.getColor(), Double.parseDouble(OrderOuttxt.getText()), flower.getSubFlowerNum(), 0, time));
                        flower.setAmount(flower.getAmount() - Double.parseDouble(OrderOuttxt.getText()));
                        createTable(listsinger, listbanyan);
                    }else{
                        JOptionPane.showMessageDialog(rootPane, "Không đủ số lượng để xuất");
                    }
                }
            }
        }
        int index = 0;
        for(singleFlower flower : listsinger){
            if(flower.getAmount() == 0){
                listsinger.remove(index);
                return;
            }
            index++;
        }
        int indexx = 0;
        for(banyanFlower flower : listbanyan){
            if(flower.getAmount() == 0){
                listbanyan.remove(indexx);
                return;
            } else {
            }
            indexx++;
        }
        createTable(listsinger, listbanyan);
    }//GEN-LAST:event_coutBtnActionPerformed

