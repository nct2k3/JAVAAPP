    private void cinBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cinBtnActionPerformed
        // TODO add your handling code here:
        Calendar Cal = Calendar.getInstance();
        int year = Cal.get(Calendar.YEAR);
        int month = Cal.get(Calendar.MONTH)+1;
        int day = Cal.get(Calendar.DAY_OF_MONTH);
        int hour = Cal.get(Calendar.HOUR_OF_DAY);
        int minute = Cal.get(Calendar.MINUTE);
        int second = Cal.get(Calendar.SECOND);
        
        String time = String.valueOf(second)+":"+String.valueOf(minute)+":"+String.valueOf(hour)+":"
                      +" "+String.valueOf(day)+"/"+String.valueOf(month)+"/"+String.valueOf(year);
        
        String code = Codetxt.getText();
        String name = Nametxt.getText();
        String type = typeFlower;
        String color = Colortxt.getText();
        String amount = Amounttxt.getText();
        String subFlower = subFlowerNumtxt.getText();
        String petalsFlower = petalsFlowerNumtxt.getText();
        double mark = 0;
        
        if(code.equals("")||name.equals("")||type.equals("")||color.equals("")||subFlower.equals("")||petalsFlower.equals("")||amount.equals("")){
            JOptionPane.showMessageDialog(rootPane, "Hãy điền đủ thông tin!");
            return;
        }
        if(Double.parseDouble(amount) <= 0){
            JOptionPane.showMessageDialog(rootPane, "Số lượng phải là số dương!");
            return;
        }
        
            listSave.add(new listSaveInput(code, name, type, color, Double.parseDouble(amount), Double.parseDouble(subFlower), Double.parseDouble(petalsFlower), time));
        
        if(type.equals("Hoa đơn")){
            for(singleFlower flower : listsinger){
                if(code.equals(flower.getCode())){
                    flower.setAmount(Double.parseDouble(amount) + flower.getAmount());
                    createTable(listsinger, listbanyan);
                    mark = 1;
                }
            }
            if(mark == 0){
                listsinger.add(new singleFlower(Double.parseDouble(petalsFlower), name, code, type, Double.parseDouble(amount), color));
                createTable(listsinger, listbanyan);
            }
        }else{
            for(banyanFlower flower : listbanyan){
                if(code.equals(flower.getCode())){
                    flower.setAmount(Double.parseDouble(amount) + flower.getAmount());
                    createTable(listsinger, listbanyan);
                    mark = 1;
                }
            }
            if(mark == 0){
                listbanyan.add(new banyanFlower(Double.parseDouble(subFlower), name, code, type, Double.parseDouble(amount), color));
                createTable(listsinger, listbanyan);
            }
            
        }
    }//GEN-LAST:event_cinBtnActionPerformed

